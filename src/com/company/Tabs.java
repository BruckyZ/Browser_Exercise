package com.company;

public class Tabs
{
	private String tittle;
	private String Url;

	public Tabs() // Empty Constructors
	{
	}

	public String getTittle()
	{
		return tittle;
	}

	public void setTittle(String tittle)
	{
		this.tittle = tittle;
	}

	public String getUrl()
	{
		return Url;
	}

	public void setUrl(String url)
	{
		Url = url;
	}
}
